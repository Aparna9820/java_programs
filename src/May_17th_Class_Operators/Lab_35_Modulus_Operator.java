package May_17th_Class_Operators;

public class Lab_35_Modulus_Operator {
    public static void main(String[] args) {
        int a=80, b=8,c=7;
        System.out.println(a%b);  //modulus means it return the Remainder  o/p-->0
        System.out.println(b%a);  // o/p-->8
        System.out.println(a%c);   // o/p-->3
        System.out.println(b%c);   // o/p-->1
        System.out.println(13%6);   // o/p-->1
    }
}
